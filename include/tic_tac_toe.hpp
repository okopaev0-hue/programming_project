#ifndef TIC_TAC_TOE_HPP
#define TIC_TAC_TOE_HPP

/**
 * @file tic_tac_toe.hpp
 * @brief Объявления функций игровой логики для консольной игры Tic Tac Toe.
 *
 * Этот заголовочный файл содержит объявления функций, которые отвечают
 * за проверку правил игры, работу с игровым полем, валидацию пользовательских
 * настроек и генерацию случайных ходов компьютера.
 *
 * Функции из этого файла используются как основной программой, так и тестами.
 */

#include <vector>
#include <utility>
#include <random>

/**
 * @brief Тип игрового поля.
 *
 * Игровое поле представлено двумерным вектором символов.
 * Каждый элемент поля содержит:
 * - пробел `' '`, если клетка свободна;
 * - символ игрока, например `'X'`, `'O'`, `'A'`, если клетка занята.
 */
using Board = std::vector<std::vector<char>>;

/**
 * @brief Возвращает символ игрока по его индексу.
 *
 * Первый игрок получает символ `'X'`, второй — `'O'`.
 * Начиная с третьего игрока используются буквы латинского алфавита:
 * третий игрок — `'A'`, четвёртый — `'B'` и так далее.
 *
 * @param playerIndex Индекс игрока, начиная с 0.
 * @return Символ, которым игрок ходит на поле.
 */
char getPlayerSymbol(int playerIndex);

/**
 * @brief Проверяет корректность выбранного режима игры.
 *
 * Допустимые режимы:
 * - `1` — режим игроков;
 * - `2` — режим компьютера против компьютера.
 *
 * @param gameMode Номер выбранного режима игры.
 * @return `true`, если режим допустим, иначе `false`.
 */
bool isValidGameMode(int gameMode);

/**
 * @brief Проверяет корректность количества игроков.
 *
 * В ручном режиме игры допускается от 2 до 9 игроков включительно.
 *
 * @param playersCount Количество игроков.
 * @return `true`, если количество игроков допустимо, иначе `false`.
 */
bool isValidPlayersCount(int playersCount);

/**
 * @brief Проверяет корректность количества раундов.
 *
 * Количество раундов должно быть не меньше 1.
 *
 * @param roundsCount Количество раундов.
 * @return `true`, если количество раундов допустимо, иначе `false`.
 */
bool isValidRoundsCount(int roundsCount);

/**
 * @brief Проверяет корректность размера игрового поля.
 *
 * Допустимые размеры поля:
 * - `3` для поля 3x3;
 * - `4` для поля 4x4;
 * - `5` для поля 5x5.
 *
 * @param boardSize Размер стороны игрового поля.
 * @return `true`, если размер поля допустим, иначе `false`.
 */
bool isValidBoardSize(int boardSize);

/**
 * @brief Проверяет, находится ли клетка внутри игрового поля.
 *
 * Координаты считаются с нуля.
 * Например, для поля 3x3 допустимыми индексами являются `0`, `1`, `2`.
 *
 * @param board Игровое поле.
 * @param row Индекс строки.
 * @param col Индекс столбца.
 * @return `true`, если клетка находится внутри поля, иначе `false`.
 */
bool isCellInside(const Board& board, int row, int col);

/**
 * @brief Проверяет, свободна ли указанная клетка.
 *
 * Клетка считается свободной, если она находится внутри поля
 * и содержит символ пробела `' '`.
 *
 * @param board Игровое поле.
 * @param row Индекс строки.
 * @param col Индекс столбца.
 * @return `true`, если клетка существует и свободна, иначе `false`.
 */
bool isCellFree(const Board& board, int row, int col);

/**
 * @brief Выполняет ход игрока.
 *
 * Если указанная клетка существует и свободна, функция записывает
 * в неё символ игрока. Если клетка занята или находится за пределами поля,
 * ход не выполняется.
 *
 * @param board Игровое поле.
 * @param row Индекс строки.
 * @param col Индекс столбца.
 * @param symbol Символ игрока.
 * @return `true`, если ход успешно выполнен, иначе `false`.
 */
bool makeMove(Board& board, int row, int col, char symbol);

/**
 * @brief Проверяет, победил ли игрок с указанным символом.
 *
 * Победа засчитывается, если символ игрока занимает:
 * - целую строку;
 * - целый столбец;
 * - главную диагональ;
 * - побочную диагональ.
 *
 * Для поля 3x3 нужно заполнить 3 клетки подряд,
 * для поля 4x4 — 4 клетки, для поля 5x5 — 5 клеток.
 *
 * @param board Игровое поле.
 * @param symbol Символ проверяемого игрока.
 * @return `true`, если игрок победил, иначе `false`.
 */
bool checkWin(const Board& board, char symbol);

/**
 * @brief Проверяет, закончилась ли игра ничьёй.
 *
 * Ничья определяется как полностью заполненное поле.
 * Функция не проверяет наличие победителя, а только отсутствие свободных клеток.
 *
 * @param board Игровое поле.
 * @return `true`, если поле полностью заполнено, иначе `false`.
 */
bool isDraw(const Board& board);

/**
 * @brief Возвращает список всех свободных клеток на поле.
 *
 * Каждая свободная клетка возвращается как пара координат:
 * `{row, col}`.
 *
 * @param board Игровое поле.
 * @return Вектор пар координат свободных клеток.
 */
std::vector<std::pair<int, int>> getFreeCells(const Board& board);

/**
 * @brief Возвращает случайную свободную клетку.
 *
 * Функция используется в режиме "Computer vs Computer".
 * Сначала она получает список всех свободных клеток, затем выбирает
 * одну из них случайным образом с помощью переданного генератора случайных чисел.
 *
 * Если свободных клеток нет, функция возвращает `{-1, -1}`.
 *
 * @param board Игровое поле.
 * @param generator Генератор случайных чисел `std::mt19937`.
 * @return Пара координат случайной свободной клетки или `{-1, -1}`.
 */
std::pair<int, int> getRandomMove(const Board& board, std::mt19937& generator);

#endif